public class InspectionQueryData {

	private String queryType;
	private String queryValue;
	private String queryLogic;
	private String queryOrgan;
	private String queryOrgan4;
	public String getQueryType() {
		return queryType;
	}
	public void setQueryType(String queryType) {
		this.queryType = queryType;
	}
	public String getQueryValue() {
		return queryValue;
	}
	public void setQueryValue(String queryValue) {
		this.queryValue = queryValue;
	}
	public String getQueryLogic() {
		return queryLogic;
	}
	public void setQueryLogic(String queryLogic) {
		this.queryLogic = queryLogic;
	}
	public String getQueryOrgan() {
		return queryOrgan;
	}
	public void setQueryOrgan(String queryOrgan) {
		this.queryOrgan = queryOrgan;
	}
	public String getQueryOrgan4() {
		return queryOrgan4;
	}
	public void setQueryOrgan4(String queryOrgan4) {
		this.queryOrgan4 = queryOrgan4;
	}
	
}
